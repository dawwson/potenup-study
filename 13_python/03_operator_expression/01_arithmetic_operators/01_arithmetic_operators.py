num_a: int = 10
num_b: int = 3

print("--- 기본 사칙 연산  ---")
print("덧셈: ", num_a + num_b)  # 13
print("뺄셈: ", num_a - num_b)  # 7
print("곱셈: ", num_a * num_b)  # 30
print("나눗셈: ", num_a / num_b)  # 3.3333...
print("정수 나눗셈: ", num_a // num_b)  # 3
print("나머지: ", num_a % num_b)  # 1
print("거듭제곱: ", num_a**num_b)  # 1000
